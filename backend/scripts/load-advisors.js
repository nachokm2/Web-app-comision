function normalizeName (name = '') {
  return name.replace(/\s+/g, ' ').trim();
}

async function run () {
  const entries = Object.entries(RAW_ADVISORS)
    .map(([id, name]) => ({
      id: Number(id),
      name: normalizeName(name)
    }))
    .filter((item) => Number.isInteger(item.id) && item.name.length);

  if (!entries.length) {
    console.log('No hay asesores para procesar.');
    process.exit(0);
  }

  const ids = entries.map((item) => item.id);
  const names = entries.map((item) => item.name);

  await db.query(
    `INSERT INTO "comision_ua"."asesores" (id, nombre_completo)
     SELECT * FROM UNNEST ($1::int[], $2::text[])
     ON CONFLICT (id) DO UPDATE SET nombre_completo = EXCLUDED.nombre_completo`,
    [ids, names]
  );

  console.log(`Se cargaron/actualizaron ${entries.length} asesores.`);
  process.exit(0);
}

run().catch((err) => {
  console.error('Fallo al cargar asesores:', err);
  process.exit(1);
});
